
function replaceImage() {
    var giftImage = document.getElementById('giftImage');
    giftImage.src = 'images/squash.jpg';
}

function showPopup() {
    var popup = document.getElementById('popup');
    popup.style.display = 'block';
}

function closePopup() {
    var popup = document.getElementById('popup');
    popup.style.display = 'none';
}

